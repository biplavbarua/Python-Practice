#Bitwise Operators Example

a = 5
b = 3

#Bitwise AND
print(f"a & b: {a & b}")

#Bitwise OR
print(f"a | b: {a | b}")

#Bitwise XOR
print(f"a ^ b: {a ^ b}")

# Bitwise NOT
print(f"~a: {~a}")
print(f"~b: {~b}")

# Left Shift
print(f"a << 1: {a << 1}")  # Shifts bits of a left by 1

# Right Shift
print(f"a >> 1: {a >> 1}")  # Shifts bits of a right by 1
