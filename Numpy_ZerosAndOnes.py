import numpy as np
arr1 = np.array([1,2,3])

#Zeros and Ones
arr2 = np.zeros((2,3))
arr3 = np.ones((3,4))

#Creating Sequences
arr4 = np.arrange(0, 10, 2)

#Generates num evenly spaced numbers between start and stop.
arr5 = np.linspace(0, 5, 10)

print(arr1)
print(arr2)
print(arr3)
print(arr4)
print(arr5)